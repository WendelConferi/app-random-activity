package com.example.sorteadornumero;

public class Atividades {

    private String desNome;
    private Integer indImportancia;

    private Integer minimoImportancia;

    private Integer somaImportancia;


    public Integer getMinimoImportancia() {
        return minimoImportancia;
    }

    public void setMinimoImportancia(Integer minimoImportancia) {
        this.minimoImportancia = minimoImportancia;
    }

    public Integer getSomaImportancia() {
        return somaImportancia;
    }

    public void setSomaImportancia(Integer somaImportancia) {
        this.somaImportancia = somaImportancia;
    }

    public String getDesNome() {
        return desNome;
    }

    public void setDesNome(String desNome) {
        this.desNome = desNome;
    }

    public Integer getIndImportancia() {
        return indImportancia;
    }

    public void setIndImportancia(Integer indImportancia) {
        this.indImportancia = indImportancia;
    }
}
